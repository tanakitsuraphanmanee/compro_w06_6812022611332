#include <stdio.h>

int main() {
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;
 
    // บรรทัดที่ 1
    printf("ค่าของตัวแปรทั้งหมด: a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);

    // บรรทัดที่ 2
    printf("ผลการคำนวณของ d + a = %.1f\n", d + a);

    // บรรทัดที่ 3
    printf("ผลการคำนวณของ a - b = %d\n", a - b);

    // บรรทัดที่ 4
    printf("ผลการคำนวณของ c * d = %.2f\n", c * d);

    // บรรทัดที่ 5
    printf("ผลการคำนวณของ a * c = %.2f\n", a * c);

    // บรรทัดที่ 6
    printf("ผลการคำนวณของ c / d = %.2f\n", c / d);

    // บรรทัดที่ 7
    printf("ผลการคำนวณของ b / c = %.2f\n", b / c);

    // บรรทัดที่ 8
    printf("ผลการคำนวณของ a %% b = %d\n", a % b); // ใช้ %% ใน printf เพื่อ escape %

    // บรรทัดที่ 9
    printf("ผลการคำนวณของ b %% a = %d\n", b % a);

    // บรรทัดที่ 10
    printf("ผลการคำนวณของ b %% d = ค่าที่ไม่สามารถใช้ได้ (เนื่องจาก %% ใช้กับ int เท่านั้น)\n");

    return 0; 
}
