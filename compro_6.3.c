#include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    A = -2 + 5 * 2;                // 5*2=10 → -2+10=8
    B = 10 / 2 * 3;                // 10/2=5 → 5*3=15
    C = 6 / 2 + 3 * (4 % 2);       // 6/2=3, 4%2=0 → 3 + 3*0 = 3
    D = (5 + 2) * 15 % 4;          // (5+2)=7 → 7*15=105 → 105%4=1
    E = 6 + 2 * 2 - 6 / 2;         // 2*2=4, 6/2=3 → 6+4-3=7
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // 3*2=6, 8/4=2, 6%5=1 → 5+6-2+1=10
    G = (4 + 3) * 2 - 10 / (2 + 3); // (4+3)=7, 2+3=5 → 7*2=14, 10/5=2 → 14-2=12

    printf("A = %d\n", A);  // 8
    printf("B = %d\n", B);  // 15
    printf("C = %d\n", C);  // 3
    printf("D = %d\n", D);  // 1
    printf("E = %d\n", E);  // 7
    printf("F = %d\n", F);  // 10
    printf("G = %d\n", G);  // 12

    return 0;
}